public class SchdInfoDTO extends DefaultDTO implements Serializable {

	private static final long serialVersionUID = -1L;

	/** Batch Schedule명- 트리거명 */
	private String schdName;

	/** Batch Scheduler 설명 */
	private String schdDesc;

	/** Scheduler에 의해 실행되는 Batch Job Group */
	private String jobGroup;

	/** Scheduler에 의해 실행되는 Batch Job명 */
	private String jobName;

	/** Batch Scheduler cron expression */
	private String cronExpression;

	/** Batch Scheduler 실행 서버 정보 */
	private String execServer;

	/** Batch Scheduler 실행 컨테이너 정보 */
	private String execContainer;

	/** 시스템구분 */
	private String sysType;

	/** 시스템ID */
	private String sysId;

	/** 트리거형태 */
	private String triggerType;
	
	/**
	 * schdName 얻기
	 *
	 * @return the schdName
	 */
	public String getSchdName() {
		return schdName;
	}

	/**
	 * schdName 설정
	 *
	 * @param schdName the schdName to set
	 */
	public void setSchdName(String schdName) {
		this.schdName = schdName;
	}

	/**
	 * schdDesc 얻기
	 *
	 * @return the schdDesc
	 */
	public String getSchdDesc() {
		return schdDesc;
	}

	/**
	 * schdDesc 설정
	 *
	 * @param schdDesc the schdDesc to set
	 */
	public void setSchdDesc(String schdDesc) {
		this.schdDesc = schdDesc;
	}

	/**
	 * jobGroup 얻기
	 *
	 * @return the jobGroup
	 */
	public String getJobGroup() {
		return jobGroup;
	}

	/**
	 * jobGroup 설정
	 *
	 * @param jobGroup the jobGroup to set
	 */
	public void setJobGroup(String jobGroup) {
		this.jobGroup = jobGroup;
	}

	/**
	 * jobName 얻기
	 *
	 * @return the jobName
	 */
	public String getJobName() {
		return jobName;
	}

	/**
	 * jobName 설정
	 *
	 * @param jobName the jobName to set
	 */
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	/**
	 * cronExpression 얻기
	 *
	 * @return the cronExpression
	 */
	public String getCronExpression() {
		return cronExpression;
	}

	/**
	 * cronExpression 설정
	 *
	 * @param cronExpression the cronExpression to set
	 */
	public void setCronExpression(String cronExpression) {
		this.cronExpression = cronExpression;
	}

	/**
	 * execServer 얻기
	 *
	 * @return the execServer
	 */
	public String getExecServer() {
		return execServer;
	}

	/**
	 * execServer 설정
	 *
	 * @param execServer the execServer to set
	 */
	public void setExecServer(String execServer) {
		this.execServer = execServer;
	}

	/**
	 * execContainer 얻기
	 *
	 * @return the execContainer
	 */
	public String getExecContainer() {
		return execContainer;
	}

	/**
	 * execContainer 설정
	 *
	 * @param execContainer the execContainer to set
	 */
	public void setExecContainer(String execContainer) {
		this.execContainer = execContainer;
	}

	/**
	 * sysType 얻기
	 *
	 * @return the sysType
	 */
	public String getSysType() {
		return sysType;
	}

	/**
	 * sysType 설정
	 *
	 * @param sysType the sysType to set
	 */
	public void setSysType(String sysType) {
		this.sysType = sysType;
	}

	/**
	 * sysId 얻기
	 *
	 * @return the sysId
	 */
	public String getSysId() {
		return sysId;
	}

	/**
	 * sysId 설정
	 *
	 * @param sysId the sysId to set
	 */
	public void setSysId(String sysId) {
		this.sysId = sysId;
	}

	/**
	 * triggerType 얻기
	 *
	 * @return the triggerType
	 */
	public String getTriggerType() {
		return triggerType;
	}

	/**
	 * triggerType 설정
	 *
	 * @param triggerType the triggerType to set
	 */
	public void setTriggerType(String triggerType) {
		this.triggerType = triggerType;
	}	
	
	
}
